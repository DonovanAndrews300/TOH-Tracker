export default function Map() {
   const [markers,setMarkers] = useState (null)
   const [isModalVisible, setModalVisible] = useState(false);
   const {getTrees} = api()
 
   const renderItem = (item) => <Image source={{uri:item.item.url}} style={{
    alignSelf:"center",
    width:200,
    height:200,
  
}}/>
   const toggleModal = () => {
    setModalVisible(!isModalVisible);
  };

   useEffect(() => {
    (async () => {  
      getTrees().then(snapshot => snapshot.docs.forEach(doc =>setMarkers([doc.data()])))

    })();
  }, []);  

  return (
    <View style={styles.container}>
      <MapView 
      initialRegion={{
      latitude: 39.9526,
      longitude: -75.1652,
      latitudeDelta: 0.5,
      longitudeDelta: 0.5,
    }}
      style={styles.map}
      customMapStyle={mapStyle}
      >
      
  
{markers &&
  markers.map((marker, index) => {
    const images = marker.treeImages.map((image, index ) => {return {"url":image, "key":index.toString()}})
   return <Marker
      key={index}
      image={require('../../../assets/icons8-tree-100.png')}
      onPress={() => toggleModal()}
      coordinate={marker.location}
      title={marker.title}
      description={marker.description}
    >
      <Modal  isVisible={isModalVisible}  onBackdropPress={() => setModalVisible(false)}>
        <View style={{backgroundColor:"white",height:"60%"}}>
          <Text>Tracked by User: {marker.username ? marker.username : "Unknown"}</Text>
          <FlatList
        data={images}
        renderItem={renderItem}
        horizontal={true}
      /> 
        </View>
      </Modal>
    </Marker>
}
  )}   
      </MapView>
    </View>
  );
}

const styles = StyleSheet.create({
  container: {
    flex: 1,
    backgroundColor: '#fff',
    alignItems: 'center',
    justifyContent: 'center',
  },
  map: {
    zIndex: -1,
    width: Dimensions.get('window').width,
    height: Dimensions.get('window').height,
  },
});